class Viaje {

    constructor(viaje_id, titulo, descripcion, ubicacion, foto, user_id_propietario) {
        this.viaje_id = viaje_id
        this.titulo = titulo
        this.descripcion = descripcion
        this.ubicacion  = ubicacion
        this.foto = foto
        this.user_id_propietario = user_id_propietario
    }
}